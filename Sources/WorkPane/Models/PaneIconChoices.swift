    /// Only symbols that `NSImage` can render on this system (avoids blank tiles).
    static let all: [String] = {
        var seen = Set<String>()
        return candidates.filter { name in
            guard seen.insert(name).inserted else { return false }
            return NSImage(systemSymbolName: name, accessibilityDescription: nil) != nil
        }
    }()

    private static let allowedCharacterSet = CharacterSet(charactersIn: "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789._")

    static func isAllowed(_ raw: String) -> Bool {
        let s = raw.trimmingCharacters(in: .whitespacesAndNewlines)
        guard !s.isEmpty, s.count <= 80 else { return false }
        if all.contains(s) { return true }
        guard s.unicodeScalars.allSatisfy({ allowedCharacterSet.contains($0) }) else { return false }
        return NSImage(systemSymbolName: s, accessibilityDescription: nil) != nil
    }
